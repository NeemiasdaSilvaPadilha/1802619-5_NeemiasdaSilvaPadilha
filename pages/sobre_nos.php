<h1 class="display-3 fw-bold">Sobre Nós</h1>
           
A comida de rua frutas em potinhos pode estar nas esquinas, calçadas, praças e largos.<br> 
As pessoas compram e já vão consumindo sem precisar parar, porém além disso se trata de um produto saudável e ideal em todas as épocas do ano,
<br> principalmente no dias quentes.
<br>
<br>É muito vendida nas feiras, carrinhos, tabuleiros, bicicletas, bancas e cestos. 
<br>Em pontos fixos dos bairros onde pode ser encontrada cotidianamente, ou com vendedores ambulantes que tangenciam o caminho dos passantes. 
<br>
<br>Seja para um café da manhã ou para lanches ao longo do dia. 
<br>
<br>Na maior parte das vezes, as frutas em potinhos é produzida de forma artesanal e em pequena escala. Isso já seria motivo para valorizá-la, 
<br>mas existem ainda muitas outras razões, como a qualidade e a importância cultural de sua produção. 
<br>Afinal, não há nada melhor do que comprar um alimento diretamente de quem faz (ou daqueles quem trazem alimentos feitos por pequenos produtores).
<br>O pote queridinho de todos é a salada de frutas, onde se misturam várias frutas no pote, dando vários sabores.

